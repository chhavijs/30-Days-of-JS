//Template Literals Script
//This script demonstrates the use of template literals to create and log strings with embedded variables and multi-line strings.


// Template Literals Script
let personName = "kittu";
let personAge = 21;

// Embedded variables in a string
let introduction = `Hello, my name is ${personName} and I am ${personAge} years old.`;
console.log(introduction);

// Multi-line string
let multiLineString = `I love dancing
in my own way and style
I just love it.`;
console.log(multiLineString);