// script that compares two numbers using different comparison
// operators and combines conditions using logical operators,

let a = 9;
let b = 4;

// Comparison Operators
console.log("a > b:", a > b);   
console.log("a < b:", a < b);   
console.log("a >= b:", a >= b); 
console.log("a <= b:", a <= b); 
console.log("a == b:", a == b); 
console.log("a === b:", a === b); 

// Logical Operators
let condition1 = (a < b); 
let condition2 = (a > 5); 

console.log("condition1 && condition2:", condition1 && condition2); 
console.log("condition1 || condition2:", condition1 || condition2); 
console.log("!condition1:", !condition1); 
