//Number Printing Script
//For Loop

function printNumbersForLoop() {
    for (let i = 1; i <= 10; i++) {
        console.log(i);
    }
}

printNumbersForLoop();

//while loop
function printNumbersWhileLoop() {
    let i = 1;
    while (i <= 10) {
        console.log(i);
        i++;
    }
}

printNumbersWhileLoop();
